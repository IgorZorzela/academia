    private void jTableNomesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableNomesMouseClicked
        String nome = ""+jTableNomes.getValueAt(jTableNomes.getSelectedRow(),1);
        conex.conecta();
            conex.executaSQL("select * from instrutor where nome='"+nome+"'");
        try {
            conex.rs.first();
               jTextFieldId_inst.setText(String.valueOf(conex.rs.getInt("id_inst")));
               jTextFieldNome.setText(conex.rs.getString("nome"));
               jTextFieldCREF.setText(String.valueOf(conex.rs.getInt("cref")));
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"Erro"+ex);
        }
           
        conex.desconecta();
        
        jButtonAlterar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
        jButtonCancelar.setEnabled(true);
        
    }//GEN-LAST:event_jTableNomesMouseClicked
