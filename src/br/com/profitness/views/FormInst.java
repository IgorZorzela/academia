       public void fillList(String Sql){
            ArrayList inf = new ArrayList();
            String [] columns = new String []{"ID", "Nome", "CREF"};
                conex.conecta();
                    conex.executaSQL(Sql);
                    
                    try{
                        conex.rs.first();
                        do{
                            inf.add(new Object[]{conex.rs.getInt("id_inst"), conex.rs.getString("nome"), conex.rs.getInt("cref")});
                            
                        }while(conex.rs.next());
                    }catch (SQLException ex){
                        JOptionPane.showMessageDialog(rootPane,"Nome "+mod.getPesquisa()+" não encontrado, tente novamente!");
                    }
                    
                    Tabela modTable = new Tabela(inf, columns);
                        jTableNomes.setModel(modTable);                        
                        jTableNomes.getColumnModel().getColumn(0).setPreferredWidth(34);
                        jTableNomes.getColumnModel().getColumn(0).setResizable(false);
                        jTableNomes.getColumnModel().getColumn(1).setPreferredWidth(200);
                        jTableNomes.getColumnModel().getColumn(1).setResizable(false);
                        jTableNomes.getColumnModel().getColumn(2).setPreferredWidth(60);
                        jTableNomes.getColumnModel().getColumn(2).setResizable(false);
                        jTableNomes.getTableHeader().setReorderingAllowed(false);
                        jTableNomes.setAutoResizeMode(jTableNomes.AUTO_RESIZE_OFF);
                        jTableNomes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);                      
                
                
                conex.desconecta();
        }
    
     
