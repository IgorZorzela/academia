    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed

        if(flag==1){
        mod.setNome(jTextFieldNome.getText());
        mod.setSenha(jTextFieldSenha.getText());
       
        JOptionPane.showMessageDialog(rootPane,"cadastrou");        
        try {        
            con.salvar(mod);
            
                jTextFieldNome.setText("");
                jTextFieldSenha.setText("");
                
                jTextFieldNome.setEnabled(false);
                jTextFieldSenha.setEnabled(false);
                jButtonGravar.setEnabled(false);
        
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane,"nao cadastrou"+ex);
        }
       } else{
            mod.setId_usu((Integer.parseInt(jTextFieldId_Usu.getText())));
            mod.setNome(jTextFieldNome.getText());
            mod.setSenha(jTextFieldSenha.getSelectedText());
            con.alterar(mod);
            //limpar campos
            jTextFieldId_Usu.setText("");
            jTextFieldNome.setText("");  
            jTextFieldSenha.setText("");
            jTextFieldPesquisa.setText("");     
            jTextFieldNome.setEnabled(false);           
            jButtonGravar.setEnabled(false);
            jButtonNovo.setEnabled(true);
            jButtonCancelar.setEnabled(false);
            
        }
       
    }//GEN-LAST:event_jButtonGravarActionPerformed

